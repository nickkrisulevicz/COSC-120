#include <iostream>
using namespace std;

const double HEIGHT = 8;
const double WIDTH = 3;

int main()
{
    double area = HEIGHT * WIDTH;
    double perimeter = (2 * HEIGHT) + (2 * WIDTH);

    cout << "Area is: " << area << endl;
    cout << "Perimeter is: " << perimeter << endl;
}
